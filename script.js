//Compound Assignment With Augmented Addition

var a = 3;
var b = 17;
var c = 12;

a += 12;
b += 9;
c += 7;

output: 
a = 15
b = 26
c = 19


//Compound Assignment With Augmented Subtraction

var a = 11;
var b = 9;
var c = 3;

a -= 6;
b -= 15;
c -= 1;

output:
a = 5
b = -6
c = 2


//Compound Assignment With Augmented Multiplication 

var a = 5;
